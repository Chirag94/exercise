package pojos;

public class SKU {

	Double unitPrice;
	Character skuID;

	public Double getUnitPrice() {
		return unitPrice;
	}

	public void setUnitPrice(Double unitPrice) {
		this.unitPrice = unitPrice;
	}

	public Character getSkuID() {
		return skuID;
	}

	public void setSkuID(Character skuID) {
		this.skuID = skuID;
	}

}
